package loopDemo;

public class main {

	public static void main(String[] args) {

		// for

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("Döngü bitti.");

		// while
		
		int i =1;
		
		while(i<10) {
			System.out.println(i);
			i++;
		}
		System.out.println("Döngü bitti.");
	}

}
